def kp_emoji(kp: Optional[float]) -> str:
    if kp is None: return "⚪"
    k = int(round(kp))
    if k in _KP_COLOR_RANGES["green"]:  return "🟢"
    if k in _KP_COLOR_RANGES["yellow"]: return "🟡"
    return "🔴"

# ───────────────────────── Погода (тренды) ───────────────────────────────

def pressure_trend(w: Dict[str, Any]) -> str:
    hp = w.get("hourly", {}).get("surface_pressure", [])
    if len(hp) < 2: return "→"
    diff = hp[1] - hp[0]
    if diff >= 2:   return "↑"
    if diff <= -2:  return "↓"
    return "→"

