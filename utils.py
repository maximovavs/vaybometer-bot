def kp_emoji(kp: Optional[float]) -> str:
    """
    Эмодзи по индексу Kp:
      k < 3  → ⚪
      3 ≤ k < 5  → 🟢
      k ≥ 5     → 🔴
      None   → ⚪
    """
    if kp is None:
        return "⚪"
    k = int(round(kp))
    if k < 3:
        return "⚪"
    if k < 5:
        return "🟢"
    return "🔴"

