# ─────────────────────── Компас, облака, ветер ───────────────────────────

COMPASS = [
    "С","ССВ","СВ","ВСВ","В","ВЮВ","ЮВ","ЮЮВ",
    "Ю","ЮЮЗ","ЮЗ","ЗЮЗ","З","ЗСЗ","СЗ","ССЗ",
]

def compass(deg: float) -> str:
    return COMPASS[int((deg/22.5)+0.5) % 16]

def clouds_word(pc: int) -> str:
    if pc < 25:  return "ясно"
    if pc < 70:  return "переменная"
    return "пасмурно"

def wind_phrase(km_h: float) -> str:
    if km_h < 2:   return "штиль"
    if km_h < 8:   return "слабый"
    if km_h < 14:  return "умеренный"
    return "сильный"

def kmh_to_ms(v_kmh: Optional[float]) -> Optional[float]:
    """Км/ч → м/с (округление до 0.1)."""
    if v_kmh is None: return None
    try:
        return round(float(v_kmh)/3.6, 1)
    except (TypeError, ValueError):
        return None

def ms_to_kmh(v_ms: Optional[float]) -> Optional[float]:
    """М/с → км/ч (округление до 0.1)."""
    if v_ms is None: return None
    try:
        return round(float(v_ms)*3.6, 1)
    except (TypeError, ValueError):
        return None

# бэкенд-совместимые алиасы
kmph_to_ms = kmh_to_ms
mps_to_kmph = ms_to_kmh

def safe(v: Any, unit: str = "") -> str:
    if v in (None, "None", "—"): return "—"
    return f"{v:.1f}{unit}" if isinstance(v, (int, float)) else f"{v}{unit}"

# ─────────────────────────── AQI / PM ────────────────────────────────────

def aqi_color(aqi: int | float | str) -> str:
    if aqi in ("—", "н/д"): return "⚪"
    try: v = float(aqi)
    except (TypeError, ValueError): return "⚪"
    if v <=  50: return "🟢"
    if v <= 100: return "🟡"
    if v <= 150: return "🟠"
    if v <= 200: return "🔴"
    if v <= 300: return "🟣"
    return "🟤"

def pm_color(pm: Optional[float | int | str], with_unit: bool = False) -> str:
    if pm in (None, "—", "н/д"):
        return "⚪ н/д"
    try:
        val = float(pm)
    except (TypeError, ValueError):
        return "⚪ н/д"
    if val <= 12:    col = "🟢"
    elif val <= 35:  col = "🟡"
    elif val <= 55:  col = "🟠"
    elif val <=150:  col = "🔴"
    elif val <=250:  col = "🟣"
    else:            col = "🟤"
    txt = f"{int(round(val))}"
    if with_unit: txt += " µg/м³"
    return f"{col}{txt}"

def smoke_index(pm25: Optional[float|int|str],
                pm10:  Optional[float|int|str]) -> Tuple[str, str]:
    def _f(x):
        try: return float(x)
        except (TypeError, ValueError): return None
    p25, p10 = _f(pm25), _f(pm10)
    if p25 is None: return "⚪", "н/д"
    lvl = 0 if p25 <= 25 else (1 if p25 <= 55 else 2)
    if p25 >= 20 and p10 and p10 > 0 and (p25/p10) > 0.70:
        lvl = min(lvl+1, 2)
    return ("🟢","низкое") if lvl==0 else (("🟡","среднее") if lvl==1 else ("🔴","высокое"))

